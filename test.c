void            test(int *nb, const char *fmt, ...)
{
    va_list     ap;
    va_list     ap2;
    char        *s1;
    char        *s2;
    int         i1;
    int         i2;
    int         diff;
   int          t4;
   int          t;
    *nb += 1;
    va_start(ap2, fmt);
    va_start(ap, fmt);
    diff = 0;
    if ((i1 = ft_vasprintf(&s1, fmt, ap)) != (i2 = vasprintf(&s2, fmt, ap2)) || (i1 >= 0 && (diff = memcmp(s1, s2, i1))))
    {
        g_errors += 1;
        printf("[test line %i] invalid output: {%i} [r=%i | libc: r=%i]\n", *nb, diff, i1, i2);
        ft_putstr("YOUR: ");
        ft_putmem(s1, i1);
        ft_putendl("");
        ft_putstr("LIBC: ");
        ft_putmem(s2, i2);
        ft_putendl("");
    }
    else
    {
        printf ("[test line %i] success r=%i\t| \"%s\"\n", *nb, i2, s2);
    }
    free(s1);
    free(s2);
    va_end(ap);
    va_end(ap2);
}

int isLittleEndian()
{
    short int number = 0x1;
    char *numPtr = (char*)&number;
    return (numPtr[0] == 1);
}

